function translatePageContent(language) {
  const elements = document.querySelectorAll("[data-key]");
  elements.forEach((element) => {
    const key = element.getAttribute("data-key");
    if (translations[language][key]) {
      element.textContent = translations[language][key];
    }
  });
}
document.addEventListener("DOMContentLoaded", function () {
  const languageSelect = document.getElementById("language-select");
  const customFileButton = document.getElementById("customFileButton");

  // Initialize language settings based on saved preferences or default
  const savedLanguage = localStorage.getItem("language") || "tr";
  changeLanguage(savedLanguage);

  languageSelect.addEventListener("change", function () {
    const selectedLanguage = this.value;
    changeLanguage(selectedLanguage);
  });

  function changeLanguage(language) {
    if (language === "en") {
      document.querySelector("html").lang = "en";
      customFileButton.innerText = "Select File";
    } else {
      document.querySelector("html").lang = "tr";
      customFileButton.innerText = "Dosya Seç";
    }

    // Save language preference to local storage or cookies
    localStorage.setItem("language", language);
  }

  // Trigger the file input dialog when the custom button is clicked
  customFileButton.addEventListener("click", function () {
    document.getElementById("imageUpload").click();
  });
});
